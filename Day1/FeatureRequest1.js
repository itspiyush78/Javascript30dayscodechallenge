
/*----------Variable Types Console Log-------------*/

// write a script that declare a variable of diffrent data types and both the value and type of each variable to the console.

// Number
let numberVar = 42;
console.log("Value:", numberVar, "| Type:", typeof numberVar); // Output: Value: 42 | Type: number

// String
let stringVar = "Hello, World!";
console.log("Value:", stringVar, "| Type:", typeof stringVar); // Output: Value: Hello, World! | Type: string

// Boolean
let booleanVar = true;
console.log("Value:", booleanVar, "| Type:", typeof booleanVar); // Output: Value: true | Type: boolean

// Object
let objectVar = { name: "Piyush", age: 20 };
console.log("Value:", objectVar, "| Type:", typeof objectVar); // Output: Value: { name: "Piyush", age: 20 } | Type: object

// Array
let arrayVar = [1, 2, 3, 4, 5];
console.log("Value:", arrayVar, "| Type:", typeof arrayVar); // Output: Value: [1, 2, 3, 4, 5] | Type: object

// Null
let nullVar = null;
console.log("Value:", nullVar, "| Type:", typeof nullVar); // Output: Value: null | Type: object

// Undefined
let undefinedVar;
console.log("Value:", undefinedVar, "| Type:", typeof undefinedVar); // Output: Value: undefined | Type: undefined




